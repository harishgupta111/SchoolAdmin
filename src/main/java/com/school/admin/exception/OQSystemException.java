package com.school.admin.exception;

public class OQSystemException extends OQException {

	/**
	 * 
	 */
	private static final long serialVersionUID = 477368487543575373L;

	public OQSystemException() {
	}

	public OQSystemException(String message) {
		super(message);
	}

	public OQSystemException(Throwable cause) {
		super(cause);
	}

	public OQSystemException(String message, Throwable cause) {
		super(message, cause);
	}



}
